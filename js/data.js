// 쉬운 비교 문구 (퍼센트 사용 금지)
function easyCompare(curr,prev,type){
  if(!prev) return '—';
  const r=curr/prev, d=curr-prev;
  if(type==='money'){
    if(r>=2) return `<span class="up">약 ${r.toFixed(1)}배 ▲</span>`;
    if(r<=0.5) return `<span class="down">약 ${(1/r).toFixed(1)}분의1 ▼</span>`;
    const m=Math.round(d/10000);
    return m>0?`<span class="up">${m}만 ▲</span>`:`<span class="down">${Math.abs(m)}만 ▼</span>`;
  }
  if(type==='patient'){
    return d>0?`<span class="up">+${d}명 ▲</span>`:(d<0?`<span class="down">${d}명 ▼</span>`:'—');
  }
  if(type==='views'){
    if(r>=2) return `<span class="up">약 ${r.toFixed(1)}배 ▲</span>`;
    if(r<=0.5) return `<span class="down">약 ${(1/r).toFixed(1)}분의1 ▼</span>`;
    const m=Math.round(d/10000);
    return m>0?`<span class="up">${Math.abs(m)}만 ▲</span>`:`<span class="down">${Math.abs(m)}만 ▼</span>`;
  }
}

